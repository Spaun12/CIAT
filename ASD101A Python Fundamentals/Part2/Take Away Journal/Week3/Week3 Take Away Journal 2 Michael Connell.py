# Accept user input for the string
my_string = input("Enter a string: ")

# Access individual characters using indexing
# Here, we use indices to access the first, second,
# and last characters of the string.
# The first character has an index of 0, the second character has an index of 4,
# and the last character has an index of -1.
first_char = my_string[0]
second_char = my_string[4]
last_char = my_string[-1]

# Print the results
print("First character: {}".format(first_char))
print("Second character: {}".format(second_char))
print("Last character: {}".format(last_char))

