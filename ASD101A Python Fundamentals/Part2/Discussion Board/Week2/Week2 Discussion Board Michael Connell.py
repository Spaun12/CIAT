# Define a function that takes two parameters and returns the sum of the parameters
def add(a, b):
    # Calculate the sum of the parameters
    result = a + b
    
    # Return the result
    return result

# Define a void function that takes one parameter and prints it
def print_parameter(param):
    # Print the parameter
    print(param)

# Call the add function and store the result in a variable
result = add(2, 3)

# Print the result
print("The sum of 2 and 3 is:", result)

# Call the print_parameter function
print_parameter("Hello, World!")
